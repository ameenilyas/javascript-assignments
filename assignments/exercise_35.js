const pets = ["dog", "cat", "fish"];

for (const petName of pets) {
  if (petName === "dog") {
    console.log(`${petName} such loyal creatures`);
  }
  if (petName === "cat") {
    console.log(`${petName} such adorable creatures`);
  }
  if (petName === "fish") {
    console.log(`${petName} such pleasing creatures`);
  }
}
console.log(`${pets} they are all pet animals, i really love my pets`);
